#include "libft.h"

size_t		ft_w_count(char const *s, char del)
{
	size_t i;
	size_t cnt;

	i = 0;
	cnt = 0;
	while (s[i] != 0)
	{
		if ((i == 0 || s[i - 1] == del) && s[i] != del)
			cnt++;
		i++;
	}
	return (cnt);
}
